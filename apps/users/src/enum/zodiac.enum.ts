export function getZodiac(dateString: string): ZodiacEnum {
  const birthDate = moment(dateString, 'YYYY-MM-DD');

  for (const sign of ZodiacDateRange) {
    const startDate = moment(sign.start, 'YYYY-MM-DD');
    const endDate = moment(sign.end, 'YYYY-MM-DD');

    if (birthDate.isBetween(startDate, endDate, null, '[]')) {
      return sign.sign as ZodiacEnum;
    }
  }

  return null;
}
